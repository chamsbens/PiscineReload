int		ft_count_if(char **tab, int (*f)(char*))
{
	int x;
	int check;

	x = 0;
	check = 0;
	while (tab[x] != 0)
	{
		if ((*f)(tab[x]) == 1)
			check++;
		x++;
	}
	return (check);
}
